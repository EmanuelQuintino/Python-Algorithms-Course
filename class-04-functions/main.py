def my_sum(num1, num2):
  total = num1 - num2
  return total

print(my_sum(2, 3))
print(my_sum(5, 6))
print(my_sum(10, 20))

def my_sub(num1, num2):
  result = num1 - num2
  return result

print(my_sub(10, 6))

def calc_avg(grade1, grade2):
  avg = (grade1 + grade2) / 2
  return avg

print(calc_avg(5, 9))
print(calc_avg(7, 8))
